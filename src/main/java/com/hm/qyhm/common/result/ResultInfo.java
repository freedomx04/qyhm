package com.hm.qyhm.common.result;

public class ResultInfo extends Result {
	
	private Object data;
	
	public ResultInfo(int code, String msg, Object data) {
		this.code = code;
		this.msg = msg;
		this.data = data;
	}

	public Object getData() {
		return data;
	}

	public void setData(Object data) {
		this.data = data;
	}
	
}
